def area(a, b): 
    '''
    Функция получает на вход два числа a и b - длины сторон прямоугольника.
    Возвращает число - площадь прямоугольника со сторонами a и b.
    '''
    return a * b 

def perimeter(a, b): 
    '''
    Функция получает на вход два числа a и b - длины сторон прямоугольника.
    Возвращает число - периметр прямоугольника со сторонами a и b.
    '''
    return (a + b) * 2